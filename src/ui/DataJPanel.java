    private void populateTable() {
    DefaultTableModel model = (DefaultTableModel) tblData.getModel();
    model.setRowCount(0);
    
    for (Product vs : history.getHistory()){
        Object[] row = new Object[3];
        row[0] = vs;
        row[1] = vs.getEmpId();
        row[2] = vs.getpTitle();
        
        model.addRow(row);
        
    }
    
    }
    

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblData.getSelectedRow();
        if (selectedRowIndex<0){
            
            JOptionPane.showMessageDialog(this, "Please select an entry to delete.");
            return;
            
        }
          DefaultTableModel model = (DefaultTableModel) tblData.getModel();
          Product selectedProduct = (Product) model.getValueAt(selectedRowIndex, 0);
          
          history.deleteProduct(selectedProduct);
          JOptionPane.showMessageDialog(this, "Employee data deleted.");
          
          populateTable();
          
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel) tblData.getModel();
         if(tblData.getSelectedRowCount() == 1) {
             
        String name = txtname.getText();
        String empId = txtempId.getText();
        String pTitle = txtposition.getText();
        
        model.setValueAt(name, tblData.getSelectedRow(), 0);
        model.setValueAt(empId, tblData.getSelectedRow(), 1);
        model.setValueAt(pTitle, tblData.getSelectedRow(), 2);

        
        JOptionPane.showMessageDialog(this, "Updated Successfully.");
        
        } else{
             if(tblData.getRowCount() == 0){
               JOptionPane.showMessageDialog(this, "Table is Empty.");  
             }else{
                 JOptionPane.showMessageDialog(this, "Select Single Row for Update."); 
             }
         }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

